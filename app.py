    try:
        with get_pg_conn() as pg:
            with pg.cursor() as cur:
                if hub_id is not None:
                    try:
                        hub_id = int(hub_id)
                    except (TypeError, ValueError):
                        return jsonify({"ok": False, "error": "Невалиден hub_id."}), 400

                    cur.execute("""
                        SELECT id
                        FROM public.sessions
                        WHERE username=%s AND order_no=%s AND hub_id=%s AND end_ts IS NULL
                        ORDER BY id DESC LIMIT 1
                    """, (user["username"], order_no, hub_id))
                    row = cur.fetchone()
                    if not row:
                        return jsonify({"ok": False, "error": "Няма активен таймер за този TRIP/Хъб."}), 400
                    sess_id = row[0]
                else:
                    cur.execute("""
                        SELECT id
                        FROM public.sessions
                        WHERE username=%s AND order_no=%s AND end_ts IS NULL
                        ORDER BY id DESC
                    """, (user["username"], order_no))
                    ids = [r[0] for r in cur.fetchall()]
                    if len(ids) == 0:
                        return jsonify({"ok": False, "error": "Няма активен таймер за този TRIP."}), 400
                    if len(ids) > 1:
                        return jsonify({"ok": False, "error": "Има няколко активни таймера за този TRIP. Посочи hub_id."}), 400
                    sess_id = ids[0]

                cur.execute("""
                    UPDATE public.sessions
                       SET end_ts = %s,
                           minutes = CASE
                                      WHEN EXTRACT(EPOCH FROM (%s - start_ts)) <= 0 THEN 0
                                      ELSE GREATEST(1, CEIL(EXTRACT(EPOCH FROM (%s - start_ts)) / 60.0))::int
                                    END
                     WHERE id = %s
                 RETURNING minutes;
                """, (now_ts, now_ts, now_ts, sess_id))
                minutes = cur.fetchone()[0]
            pg.commit()

        return jsonify({"ok": True, "order_no": order_no, "hub_id": hub_id, "end_ts": now_ts.isoformat(), "minutes": minutes})
    except Exception as e:
        return jsonify({"ok": False, "error": f"PG: {e}"}), 500

