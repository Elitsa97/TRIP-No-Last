@app.post("/login")
def do_login():
    data = request.form or {}
    username = (data.get("username") or "").strip()
    password = data.get("password") or ""

    if not username or not password:
        return render_template("login.html",
                               error="Въведи потребителско име и парола.",
                               username=username)

    # 1) Опит: public.users → допускаме само superadmin от тази таблица
    try:
        with get_pg_conn() as pg:
            with pg.cursor(cursor_factory=RealDictCursor) as cur:
                cur.execute("""
                    SELECT id, username, password_hash, role, TRUE AS is_active
                    FROM public.users
                    WHERE username = %s
                    LIMIT 1
                """, (username,))
                urow = cur.fetchone()
    except Exception as e:
        return render_template("login.html",
                               error=f"PG връзка (users): {e}",
                               username=username)

    if urow and (urow.get("role") or "").lower() == "superadmin":
        if urow.get("password_hash") and check_password_hash(urow["password_hash"], password):
            session.clear()
            session["username"]     = urow["username"]
            session["auth_source"]  = "pg"
            session["pg_table"]     = "users"          # <-- важно
            session["role"]         = "superadmin"
            session["user_id"]      = urow.get("id")
            return redirect(url_for("index"))

    # 2) Опит: public.admin_users → admin/user
    try:
        with get_pg_conn() as pg:
            with pg.cursor(cursor_factory=RealDictCursor) as cur:
                cur.execute("""
                    SELECT id, username, password_hash, role, COALESCE(is_active, TRUE) AS is_active
                    FROM public.admin_users
                    WHERE username = %s
                    LIMIT 1
                """, (username,))
                arow = cur.fetchone()
    except Exception as e:
        return render_template("login.html",
                               error=f"PG връзка (admin_users): {e}",
                               username=username)

    if arow and arow["is_active"] and arow.get("password_hash") and check_password_hash(arow["password_hash"], password):
        session.clear()
        session["username"]     = arow["username"]
        session["auth_source"]  = "pg"
        session["pg_table"]     = "admin_users"
        session["role"]         = (arow["role"] or "").lower()
        session["user_id"]      = arow.get("id")
        return redirect(url_for("index"))

    return render_template("login.html", error="Грешно потребителско име или парола.", username=username)

