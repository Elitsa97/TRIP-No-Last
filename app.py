def fetch_trip_hubs_from_pg(schema: str = None, table: str = None):
    schema = schema or os.environ.get("PG_SCHEMA", "public")
    table  = table  or os.environ.get("PG_TABLE", "NVT Route$0d6519c0-93fa-42a6-9b")
    trip_col = os.environ.get("PG_TRIP_COL", "Trip No_")
    hub_col  = os.environ.get("PG_HUB_COL", "Address name")

    mapping = {}
    pg = get_pg_conn()
    try:
        q = sql.SQL("SELECT {trip}, {hub} FROM {schema}.{table}").format(
            trip=sql.Identifier(trip_col),
            hub=sql.Identifier(hub_col),
            schema=sql.Identifier(schema),
            table=sql.Identifier(table),
        )
        with pg.cursor() as cur:
            cur.execute(q)
            for trip_no, hub_name in cur.fetchall():
                mapping[str(trip_no)] = str(hub_name) if hub_name is not None else None
    finally:
        pg.close()
    return mapping

def ensure_pg_admin_table():
    try:
        with get_pg_conn() as pg:
            with pg.cursor() as cur:
                # 1) pgcrypto
                cur.execute("CREATE EXTENSION IF NOT EXISTS pgcrypto;")

                # 2) базова таблица (без plaintext)
                cur.execute("""
                    CREATE TABLE IF NOT EXISTS public.admin_users(
                      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                      username TEXT NOT NULL UNIQUE,
                      password_hash TEXT NOT NULL,
                      role TEXT NOT NULL,
                      owner_admin_username TEXT,
                      is_active BOOLEAN NOT NULL DEFAULT TRUE,
                      password_encrypted BYTEA,
                      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
                      updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
                      last_password_viewed_at TIMESTAMPTZ,
                      last_password_viewed_by  TEXT
                    );
                """)

                # 3) алтернативно: ако мигрираме от стара таблица – добави липсващи колони
                cur.execute("""
                    DO $$
                    BEGIN
                      IF NOT EXISTS (
                        SELECT 1 FROM information_schema.columns
                        WHERE table_schema='public' AND table_name='admin_users' AND column_name='owner_admin_username'
                      ) THEN
                        ALTER TABLE public.admin_users ADD COLUMN owner_admin_username TEXT;
                      END IF;

                      IF NOT EXISTS (
                        SELECT 1 FROM information_schema.columns
                        WHERE table_schema='public' AND table_name='admin_users' AND column_name='is_active'
                      ) THEN
                        ALTER TABLE public.admin_users ADD COLUMN is_active BOOLEAN NOT NULL DEFAULT TRUE;
                      END IF;

                      IF NOT EXISTS (
                        SELECT 1 FROM information_schema.columns
                        WHERE table_schema='public' AND table_name='admin_users' AND column_name='password_encrypted'
                      ) THEN
                        ALTER TABLE public.admin_users ADD COLUMN password_encrypted BYTEA;
                      END IF;

                      IF NOT EXISTS (
                        SELECT 1 FROM information_schema.columns
                        WHERE table_schema='public' AND table_name='admin_users' AND column_name='updated_at'
                      ) THEN
                        ALTER TABLE public.admin_users ADD COLUMN updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW();
                      END IF;

                      -- ако все още има password_plain → отстрани го
                      IF EXISTS (
                        SELECT 1 FROM information_schema.columns
                        WHERE table_schema='public' AND table_name='admin_users' AND column_name='password_plain'
                      ) THEN
                        ALTER TABLE public.admin_users DROP COLUMN password_plain;
                      END IF;
                    END $$;
                """)
            pg.commit()
        print("[PG] admin_users ensured (schema aligned)", flush=True)
    except Exception as e:
        print("[PG] ensure_pg_admin_table error:", e, flush=True)


def pg_upsert_credential(
    username: str,
    password_plain: str | None,
    password_hash: str,
    role: str,
    owner_admin_username: str | None = None,
    is_active: bool = True,
):
    enc_key = os.environ.get("PG_ENC_KEY", "CHANGE_ME")

    if role == "superadmin":
        owner_admin_username = None

    with get_pg_conn() as pg:
        with pg.cursor() as cur:
            cur.execute("""
                INSERT INTO public.admin_users
                    (username, password_hash, role, owner_admin_username,
                     is_active, password_encrypted, created_at, updated_at)
                VALUES (
                    %s, %s, %s, %s,
                    %s,
                    CASE WHEN %s IS NOT NULL THEN pgp_sym_encrypt(%s, %s) ELSE NULL END,
                    NOW(), NOW()
                )
                ON CONFLICT (username) DO UPDATE
                   SET password_hash = EXCLUDED.password_hash,
                       role          = EXCLUDED.role,
                       owner_admin_username = COALESCE(EXCLUDED.owner_admin_username, public.admin_users.owner_admin_username),
                       is_active     = EXCLUDED.is_active,
                       password_encrypted = CASE
                           WHEN EXCLUDED.password_encrypted IS NOT NULL THEN EXCLUDED.password_encrypted
                           ELSE public.admin_users.password_encrypted
                       END,
                       updated_at    = NOW();
            """, (
                username, password_hash, role, owner_admin_username,
                is_active,
                password_plain, password_plain, enc_key
            ))
        pg.commit()


