    with get_pg_conn() as pg:
        with pg.cursor() as cur:
            cur.execute("SELECT role FROM public.admin_users WHERE username=%s", (old_username,))
            r = cur.fetchone()
            if not r:
                return jsonify({"ok": False, "error": "Няма такъв потребител."}), 404
            if r[0] == "superadmin":
                return jsonify({"ok": False, "error": "Не може да се преименува superadmin."}), 403
