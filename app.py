def sync_trip_hub_map_from_bc():
    """
    Попълва/обновява public.trip_hub_map от public.bc_trips (по нормализирани имена),
    за да може приложението да вижда allowed триповете.
    """
    with get_pg_conn() as pg:
        with pg.cursor() as cur:
            cur.execute("""
                INSERT INTO public.trip_hub_map (trip_no, hub_id, trip_no_norm, address_name_norm)
                SELECT b.trip_no, h.id, b.trip_no_norm, b.address_name_norm
                FROM public.bc_trips b
                JOIN public.hubs h
                  ON h.name_norm = b.address_name_norm
                ON CONFLICT (trip_no) DO UPDATE
                  SET hub_id = EXCLUDED.hub_id,
                      trip_no_norm = EXCLUDED.trip_no_norm,
                      address_name_norm = EXCLUDED.address_name_norm;
            """)
        pg.commit()

def normalize_trip_no(s: str | None) -> str | None:
    if not s: return s
    s = s.strip()
    s = s.replace('–','-').replace('−','-').replace('‑','-')  # различни unicode тирета -> '-'
    s = s.replace('\u00A0',' ').replace('\u202F',' ')
    s = " ".join(s.split())
    return s.upper()

def ensure_trip_prefix(s: str | None) -> str | None:
    s = normalize_trip_no(s)
    if not s: return s
    if not s.startswith("TRIP-"):
        s = "TRIP-" + s.lstrip("TRIP-")
    return s


