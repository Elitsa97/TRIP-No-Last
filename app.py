@app.get("/admin/history_data")
@admin_or_super_required_json
def admin_history_data():
    current = get_current_user()
    order_no = (request.args.get("order_no") or "").strip()
    username = (request.args.get("username") or "").strip()
    from_d   = (request.args.get("from") or "").strip()
    to_d     = (request.args.get("to") or "").strip()

    is_super = (current["role"] == "superadmin")

    # 1) allowed hubs за admin (superadmin няма нужда)
    allowed_hub_ids = None
    if not is_super:
        try:
            with get_pg_conn() as pg:
                with pg.cursor() as cur:
                    cur.execute("""
                        SELECT ha.hub_id
                        FROM public.hubs_admins ha
                        WHERE LOWER(ha.admin_username) = LOWER(%s)
                    """, (current["username"],))
                    allowed_hub_ids = [int(r[0]) for r in cur.fetchall() if r and r[0] is not None]
        except Exception as e:
            return jsonify({"ok": False, "error": f"PG (hubs_admins): {e}"}), 500

        if not allowed_hub_ids:
            return jsonify({"ok": True, "results": []})

    # 2) Основна заявка:
    # - JOIN hubs по sessions.hub_id (ако го има)
    # - LATERAL инференция на hub, когато s.hub_id е NULL
    # - Филтър за admin: или s.hub_id ∈ allowed, или inferred ∈ allowed
    parts = [
        "SELECT",
        "  s.username,",
        "  s.order_no,",
        "  to_char(s.start_ts AT TIME ZONE 'Europe/Sofia','YYYY-MM-DD') AS start_day,",
        "  to_char(s.start_ts AT TIME ZONE 'Europe/Sofia','HH24:MI:SS') AS start_time,",
        "  to_char(s.end_ts   AT TIME ZONE 'Europe/Sofia','YYYY-MM-DD') AS end_day,",
        "  to_char(s.end_ts   AT TIME ZONE 'Europe/Sofia','HH24:MI:SS') AS end_time,",
        "  s.minutes,",
        "  COALESCE(h.name, inf.name) AS hub_display,",
        "  COALESCE(s.hub_id, inf.id) AS hub_id_display",
        "FROM public.sessions s",
        "JOIN public.admin_users au ON LOWER(au.username) = LOWER(s.username)",
        "LEFT JOIN public.hubs h ON h.id = s.hub_id",
        # инференция: ако hub_id липсва → вземи един hub от trip_hub_map
        "LEFT JOIN LATERAL (",
        "  SELECT h2.id, h2.name",
        "  FROM public.trip_hub_map m2",
        "  JOIN public.hubs h2 ON h2.id = m2.hub_id",
        "  WHERE m2.trip_no = s.order_no",
        "    AND s.hub_id IS NULL",
        # за admin ограничаваме инференцията в allowed списъка:
        ("    AND m2.hub_id = ANY(%s)" if not is_super else ""),
        "  ORDER BY h2.id",
        "  LIMIT 1",
        ") inf ON TRUE",
    ]
    where = []
    params = []

    # параметри за инференцията (само за admin)
    if not is_super:
        params.append(list(allowed_hub_ids))

    # UI филтри
    if order_no:
        order_no = ensure_trip_prefix(order_no)
        where.append("s.order_no = %s")
        params.append(order_no)

    if username:
        where.append("LOWER(s.username) = LOWER(%s)")
        params.append(username)

    if from_d:
        where.append("DATE(s.start_ts) >= %s")
        params.append(from_d)

    if to_d:
        where.append("DATE(s.start_ts) <= %s")
        params.append(to_d)

    # филтър за admin: включи само allowed записи
    if not is_super:
        where.append("("
                     " (s.hub_id IS NOT NULL AND s.hub_id = ANY(%s))"
                     " OR "
                     " (s.hub_id IS NULL AND inf.id IS NOT NULL)"
                     ")")
        params.append(list(allowed_hub_ids))

    sql = "\n".join(parts)
    if where:
        sql += "\nWHERE " + " AND ".join(where)
    sql += "\nORDER BY s.start_ts DESC\nLIMIT 500"

    try:
        with get_pg_conn() as pg:
            with pg.cursor(cursor_factory=RealDictCursor) as cur:
                cur.execute(sql, tuple(params) if params else None)
                rows = cur.fetchall()
    except Exception as e:
        # Диагностика
        print("[SQL DEBUG] -------------------------")
        print(sql)
        print("params:", params)
        return jsonify({"ok": False, "error": str(e)}), 500

    results = [{
        "username":   r["username"],
        "order_no":   r["order_no"],
        "hub":        r["hub_display"],
        "start_day":  r["start_day"],
        "start_time": r["start_time"],
        "end_day":    r["end_day"],
        "end_time":   r["end_time"],
        "minutes":    r["minutes"],
        "hub_id":     r["hub_id_display"],
    } for r in rows]
    return jsonify({"ok": True, "results": results})

