@app.post("/start")
@login_required_json
def start_timer():
    user = get_current_user()
    if user["role"] in ("admin", "superadmin"):
        return jsonify({"ok": False, "error": "Администраторът не може да стартира таймер."}), 403

    data = request.get_json() or {}
    raw = (data.get("order_no") or "").strip()
    order_no = ensure_trip_prefix(raw)
    hub_id_in = data.get("hub_id")  # може да липсва

    if not order_no:
        return jsonify({"ok": False, "error": "Въведи номер на TRIP-a."}), 400

    try:
        with get_pg_conn() as pg:
            # 1) Списък от хъбове за TRIP
            with pg.cursor() as cur:
                cur.execute("""
                    SELECT m.hub_id
                    FROM public.trip_hub_map m
                    WHERE m.trip_no = %s
                """, (order_no,))
                trip_hubs = {int(r[0]) for r in cur.fetchall() if r and r[0] is not None}

            # ако няма map – опитай live-мапинг от bc_trips + hub_aliases и кеширай
            if not trip_hubs:
                with pg.cursor() as cur:
                    cur.execute("""
                        INSERT INTO public.trip_hub_map (trip_no, hub_id)
                        SELECT DISTINCT b.trip_no, ha.hub_id
                        FROM public.bc_trips b
                        JOIN public.hub_aliases ha
                          ON ha.alias_norm = public.norm_text(b.address_name)
                        WHERE b.trip_no = %s
                        ON CONFLICT (trip_no, hub_id) DO NOTHING
                        RETURNING hub_id
                    """, (order_no,))
                    trip_hubs = {int(r[0]) for r in cur.fetchall()}

            if not trip_hubs:
                return jsonify({"ok": False, "error": "TRIP не е мапнат към нито един Хъб."}), 400

            # 2) Allowed хъбове за този user
            allowed = get_allowed_hub_ids_for_user(user)

            # 3) Определи hub_id
            hub_id: int | None = None
            if hub_id_in is not None:
                try:
                    hub_id = int(hub_id_in)
                except (TypeError, ValueError):
                    return jsonify({"ok": False, "error": "Липсва или е невалиден hub_id."}), 400
                if hub_id not in trip_hubs:
                    return jsonify({"ok": False, "error": "Този TRIP не е валиден за избрания Хъб."}), 400
                if not is_user_allowed_for_hub(pg, hub_id, user["username"], user["role"]):
                    return jsonify({"ok": False, "error": "Нямаш права за този Хъб."}), 403
            else:
                # fallback: опитай да намериш точно един allowed хъб за този TRIP
                candidates = [hid for hid in trip_hubs if hid in allowed]
                if len(candidates) == 0:
                    return jsonify({"ok": False, "error": "TRIP е мапнат, но нямаш права за нито един Хъб."}), 403
                if len(candidates) > 1:
                    return jsonify({"ok": False, "error": "TRIP е валиден за няколко Хъба. Избери конкретен Хъб."}), 400
                hub_id = int(candidates[0])

            # 4) Забрани втори активен таймер за същия TRIP+HUB от този user
            now_ts = datetime.now(timezone.utc)
            with pg.cursor() as cur:
                cur.execute("""
                    SELECT 1
                    FROM public.sessions
                    WHERE username = %s AND order_no = %s AND hub_id = %s AND end_ts IS NULL
                    LIMIT 1
                """, (user["username"], order_no, hub_id))
                if cur.fetchone():
                    return jsonify({"ok": False, "error": "Вече имаш активен таймер за този TRIP и Хъб."}), 400

                cur.execute("""
                    INSERT INTO public.sessions (order_no, hub_id, username, start_ts)
                    VALUES (%s, %s, %s, %s)
                """, (order_no, hub_id, user["username"], now_ts))
            pg.commit()

            return jsonify({"ok": True, "order_no": order_no, "hub_id": hub_id, "start_ts": now_ts.isoformat()})

    except Exception as e:
        return jsonify({"ok": False, "error": f"PG: {e}"}), 500
    
